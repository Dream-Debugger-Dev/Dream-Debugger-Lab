# Arithmetic Operators
a = 10
b = 3
print("The type of operator in python is:", type(a))
print("Addition:", a + b)          # Addition
print("Subtraction:", a - b)       # Subtraction
print("Multiplication:", a * b)    # Multiplication
print("Division:", a / b)          # Division
print("Modulus:", a % b)           # Modulus
print("Exponentiation:", a ** b)   # Exponentiation
print("Floor Division:", a // b)   # Floor Division
print("The type of operator result is:", type(a + b))

#An operator is a special symbol or keyword that is used to perform operations on variables and values. In Python, operators are used to perform various operations such as arithmetic calculations, comparisons, logical operations, and more. The type() function in Python is used to determine the type of a variable or value.
#In the above code, we demonstrate the use of arithmetic operators in Python and also show how to use the type() function to check the type of a variable and the result of an operation.
#When you run this code, it will display the results of various arithmetic operations along with the type of the variable 'a' and the type of the result of the addition operation.
